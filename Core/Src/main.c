	  HAL_Delay(500);

      // Enable TX and send data
      RS485_EnableTX();
      HAL_UART_Transmit(&huart3, tx_buff_read_co2, sizeof(tx_buff_read_co2), HAL_MAX_DELAY);

      // Switch to RX and receive data
      RS485_EnableRX();
      HAL_UART_Receive(&huart3, rx_buff_co2, sizeof(rx_buff_co2), HAL_MAX_DELAY);

      // get CO2 concentration
      co2 = (int)rx_buff_co2[3] * 100 + ((int)rx_buff_co2[4]);

      sprintf(txt_buffer, "CO2 now is %d.%d %%", co2/100, co2%100);

      HAL_UART_Transmit(&huart1, (uint8_t*)txt_buffer, strlen(txt_buffer), HAL_MAX_DELAY);

      // control
      if (co2 < thresh_low)
      {
    	  HAL_GPIO_WritePin(LED2_Relay_GPIO_Port, LED2_Relay_Pin, GPIO_PIN_SET);
      }

      if (co2 > thresh_high)
      {
    	  HAL_GPIO_WritePin(LED2_Relay_GPIO_Port, LED2_Relay_Pin, GPIO_PIN_RESET);
      }

    /* USER CODE END WHILE */

