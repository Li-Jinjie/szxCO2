  uint8_t tx_buff_read_co2[] = {0x01, 0x03, 0x00, 0x02, 0x00, 0x01, 0x25, 0xCA};
//  uint8_t rx_buff_addr[9];
  uint8_t rx_buff_co2[7];

  char txt_buffer[50]; // Buffer to hold the converted string

  int co2 = 0;  // percentage * 100

